create or replace function public.marketplace_listing_violation(p_text text)
returns text
language plpgsql immutable set search_path = public as $$
declare v text := lower(coalesce(p_text,''));
begin
  if v ~ '(枪支|手枪|步枪|弹药|子弹|消音器|firearm|handgun|rifle|ammunition|suppressor)' then return 'weapons'; end if;
  if v ~ '(毒品|大麻|可卡因|海洛因|处方药|迷幻|cocaine|heroin|fentanyl|prescription drug|marijuana)' then return 'controlled_substances'; end if;
  if v ~ '(电子烟油|烟弹|vape juice|nicotine liquid|烟草制品)' then return 'regulated_tobacco'; end if;
  if v ~ '(假货|高仿|复刻|盗版|仿冒|counterfeit|replica|knockoff|pirated)' then return 'counterfeit'; end if;
  if v ~ '(赃物|偷来|盗窃|stolen goods|stolen)' then return 'stolen_goods'; end if;
  if v ~ '(成人服务|裸照|色情|色情服务|escort|pornographic|adult service)' then return 'adult_content'; end if;
  return null;
end;
$$;

create or replace function public.marketplace_guard_personal_listing()
returns trigger language plpgsql set search_path = public as $$
declare v_reason text;
begin
  v_reason := public.marketplace_listing_violation(concat_ws(' ', new.title, new.description, new.category));
  if v_reason is not null then raise exception 'listing_restricted:%', v_reason using errcode = '22023'; end if;
  return new;
end;
$$;

drop trigger if exists marketplace_guard_personal_listing on public.personal_shop_products;
create trigger marketplace_guard_personal_listing before insert or update of title, description, category
on public.personal_shop_products for each row execute function public.marketplace_guard_personal_listing();

create or replace function public.marketplace_guard_merchant_products()
returns trigger language plpgsql set search_path = public as $$
declare v_item jsonb; v_reason text;
begin
  for v_item in select value from jsonb_array_elements(coalesce(new.products, '[]'::jsonb)) loop
    v_reason := public.marketplace_listing_violation(concat_ws(' ', v_item->>'name', v_item->>'title', v_item->>'description', v_item->>'category', v_item->>'categories'));
    if v_reason is not null then raise exception 'listing_restricted:%', v_reason using errcode = '22023'; end if;
  end loop;
  return new;
end;
$$;

drop trigger if exists marketplace_guard_merchant_products on public.merchants;
create trigger marketplace_guard_merchant_products before insert or update of products
on public.merchants for each row execute function public.marketplace_guard_merchant_products();

create or replace function public.marketplace_public_listing_controls(p_owner_user_id uuid, p_source_type text)
returns table(source_id text, status text)
language sql stable security definer set search_path = public as $$
  select source_id, status
  from public.marketplace_listing_controls
  where owner_user_id = p_owner_user_id
    and source_type = p_source_type
    and status in ('frozen','removed');
$$;

create or replace function public.marketplace_report_listing(
  p_owner_user_id uuid,
  p_source_type text,
  p_source_id text,
  p_listing_title text,
  p_reason text,
  p_detail text default ''
)
returns jsonb language plpgsql security definer set search_path = public as $$
declare v_report public.marketplace_listing_reports%rowtype;
begin
  if auth.uid() is null then raise exception 'login_required'; end if;
  if p_owner_user_id is null or p_owner_user_id = auth.uid() then raise exception 'invalid_report_target'; end if;
  if p_source_type not in ('personal_shop_product','merchant_product') then raise exception 'invalid_report_target'; end if;
  if char_length(trim(coalesce(p_source_id,''))) = 0 then raise exception 'invalid_report_target'; end if;
  if p_reason not in ('prohibited','counterfeit','fraud','unsafe','stolen','privacy','other') then raise exception 'invalid_report_reason'; end if;
  insert into public.marketplace_listing_reports(reporter_user_id,owner_user_id,source_type,source_id,listing_title,reason,detail)
  values(auth.uid(),p_owner_user_id,p_source_type,trim(p_source_id),left(trim(coalesce(p_listing_title,'')),160),p_reason,left(trim(coalesce(p_detail,'')),800))
  on conflict(reporter_user_id,source_type,source_id) do update set reason=excluded.reason,detail=excluded.detail,status='pending',handled_by=null,handled_at=null,created_at=now()
  returning * into v_report;
  return to_jsonb(v_report);
end;
$$;

create or replace function public.marketplace_moderate_listing(
  p_owner_user_id uuid,
  p_source_type text,
  p_source_id text,
  p_status text,
  p_reason_code text default null,
  p_note text default null
)
returns jsonb language plpgsql security definer set search_path = public as $$
declare v_control public.marketplace_listing_controls%rowtype;
begin
  if not exists(select 1 from public.deal_admins where user_id=auth.uid()) then raise exception 'admin_required'; end if;
  if p_source_type not in ('personal_shop_product','merchant_product') or p_status not in ('active','frozen','removed') then raise exception 'invalid_listing_moderation'; end if;
  insert into public.marketplace_listing_controls(owner_user_id,source_type,source_id,status,reason_code,moderator_note,reviewed_by,reviewed_at)
  values(p_owner_user_id,p_source_type,trim(p_source_id),p_status,nullif(trim(coalesce(p_reason_code,'')),''),nullif(trim(coalesce(p_note,'')),''),auth.uid(),now())
  on conflict(owner_user_id,source_type,source_id) do update set status=excluded.status,reason_code=excluded.reason_code,moderator_note=excluded.moderator_note,reviewed_by=excluded.reviewed_by,reviewed_at=excluded.reviewed_at,updated_at=now()
  returning * into v_control;
  update public.marketplace_listing_reports set status=case when p_status='active' then 'dismissed' else 'resolved' end,handled_by=auth.uid(),handled_at=now()
  where owner_user_id=p_owner_user_id and source_type=p_source_type and source_id=trim(p_source_id) and status in ('pending','reviewing');
  return to_jsonb(v_control);
end;
$$;

create or replace function public.marketplace_order_event_from_retail()
returns trigger language plpgsql security definer set search_path = public as $$
declare v_actor uuid := auth.uid();
begin
  if tg_op = 'INSERT' then
    insert into public.marketplace_order_events(order_type,order_id,order_code,actor_user_id,event_type,next_status,payload)
    values('merchant_retail',new.id,new.order_code,coalesce(v_actor,new.customer_user_id),'created',new.status,jsonb_build_object('subtotal',new.subtotal,'item_count',new.item_count));
  elsif old.status is distinct from new.status then
    insert into public.marketplace_order_events(order_type,order_id,order_code,actor_user_id,event_type,previous_status,next_status,payload)
    values('merchant_retail',new.id,new.order_code,coalesce(v_actor,new.merchant_user_id),'status_changed',old.status,new.status,jsonb_build_object('pickup_at',new.pickup_at,'merchant_note',new.merchant_note));
  end if;
  return new;
end;
$$;

drop trigger if exists marketplace_retail_order_event on public.merchant_retail_orders;
create trigger marketplace_retail_order_event after insert or update on public.merchant_retail_orders
for each row execute function public.marketplace_order_event_from_retail();

create or replace function public.marketplace_accept_merchant_agreement(p_version text default '2026-07-23')
returns jsonb language plpgsql security definer set search_path = public as $$
declare v_row public.merchant_platform_agreements%rowtype;
begin
  if auth.uid() is null or not exists(select 1 from public.merchants where user_id=auth.uid() and verified=true) then raise exception 'merchant_required'; end if;
  insert into public.merchant_platform_agreements(merchant_user_id,agreement_version,accepted_ip)
  values(auth.uid(),left(trim(coalesce(p_version,'2026-07-23')),40),nullif(current_setting('request.headers',true)::jsonb->>'x-forwarded-for',''))
  on conflict(merchant_user_id,agreement_key,agreement_version) do update set accepted_at=now()
  returning * into v_row;
  return to_jsonb(v_row);
end;
$$;

create or replace function public.marketplace_open_retail_dispute(p_order_id uuid,p_reason text,p_detail text default '')
returns jsonb language plpgsql security definer set search_path = public as $$
declare v_order public.merchant_retail_orders%rowtype; v_dispute public.marketplace_order_disputes%rowtype;
begin
  if auth.uid() is null then raise exception 'login_required'; end if;
  if p_reason not in ('not_received','not_as_described','damaged','merchant_cancelled','other') then raise exception 'invalid_dispute_reason'; end if;
  select * into v_order from public.merchant_retail_orders where id=p_order_id for update;
  if not found or auth.uid() not in (v_order.customer_user_id,v_order.merchant_user_id) then raise exception 'order_not_found'; end if;
  if v_order.status='cancelled' then raise exception 'order_closed'; end if;
  insert into public.marketplace_order_disputes(order_type,order_id,opened_by,merchant_user_id,reason,detail)
  values('merchant_retail',v_order.id,auth.uid(),v_order.merchant_user_id,p_reason,left(trim(coalesce(p_detail,'')),1200))
  on conflict(order_type,order_id) do update set reason=excluded.reason,detail=excluded.detail,status='open',opened_by=excluded.opened_by,updated_at=now(),resolution=null,resolved_at=null,resolved_by=null
  returning * into v_dispute;
  update public.merchant_retail_orders set dispute_status='open' where id=v_order.id;
  insert into public.marketplace_order_events(order_type,order_id,order_code,actor_user_id,event_type,previous_status,next_status,payload)
  values('merchant_retail',v_order.id,v_order.order_code,auth.uid(),'dispute_opened',v_order.dispute_status,'open',jsonb_build_object('reason',p_reason));
  return to_jsonb(v_dispute);
end;
$$;

create or replace function public.marketplace_retail_order_history(p_order_id uuid)
returns jsonb language plpgsql security definer set search_path = public as $$
declare v_order public.merchant_retail_orders%rowtype;
begin
  if auth.uid() is null then raise exception 'login_required'; end if;
  select * into v_order from public.merchant_retail_orders where id=p_order_id;
  if not found or (auth.uid() not in (v_order.customer_user_id,v_order.merchant_user_id) and not exists(select 1 from public.deal_admins where user_id=auth.uid())) then raise exception 'order_not_found'; end if;
  return jsonb_build_object('order',to_jsonb(v_order),'events',coalesce((select jsonb_agg(to_jsonb(e) order by e.created_at asc) from public.marketplace_order_events e where e.order_type='merchant_retail' and e.order_id=v_order.id),'[]'::jsonb),'dispute',(select to_jsonb(d) from public.marketplace_order_disputes d where d.order_type='merchant_retail' and d.order_id=v_order.id));
end;
$$;

