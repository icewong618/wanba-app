-- Public personal shop pages never return paused listings.
create or replace function public.personal_shop_public_snapshot(p_market text, p_slug text)
returns jsonb language sql stable security definer set search_path=public as $$
  select jsonb_build_object(
    'shop', (select to_jsonb(s) from public.personal_shops s where s.market_code=lower(trim(p_market)) and s.slug=lower(trim(p_slug)) and s.status='active'),
    'products', coalesce((select jsonb_agg(to_jsonb(p) order by p.created_at desc) from public.personal_shop_products p join public.personal_shops s on s.user_id=p.shop_user_id where s.market_code=lower(trim(p_market)) and s.slug=lower(trim(p_slug)) and s.status='active' and p.status='active' and not exists(select 1 from public.marketplace_listing_controls c where c.owner_user_id=p.shop_user_id and c.source_type='personal_shop_product' and c.source_id=p.id::text and c.status in ('frozen','removed'))),'[]'::jsonb)
  );
$$;
