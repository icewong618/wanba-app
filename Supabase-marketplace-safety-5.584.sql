create table if not exists public.marketplace_listing_controls (
  id uuid primary key default gen_random_uuid(),
  owner_user_id uuid not null references auth.users(id) on delete cascade,
  source_type text not null check (source_type in ('personal_shop_product','merchant_product')),
  source_id text not null check (char_length(trim(source_id)) between 1 and 120),
  status text not null default 'active' check (status in ('active','frozen','removed')),
  reason_code text,
  moderator_note text,
  reviewed_by uuid references auth.users(id) on delete set null,
  reviewed_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (owner_user_id, source_type, source_id)
);

create table if not exists public.marketplace_listing_reports (
  id uuid primary key default gen_random_uuid(),
  reporter_user_id uuid not null references auth.users(id) on delete cascade,
  owner_user_id uuid not null references auth.users(id) on delete cascade,
  source_type text not null check (source_type in ('personal_shop_product','merchant_product')),
  source_id text not null check (char_length(trim(source_id)) between 1 and 120),
  listing_title text not null default '' check (char_length(listing_title) <= 160),
  reason text not null check (reason in ('prohibited','counterfeit','fraud','unsafe','stolen','privacy','other')),
  detail text not null default '' check (char_length(detail) <= 800),
  status text not null default 'pending' check (status in ('pending','reviewing','resolved','dismissed')),
  handled_by uuid references auth.users(id) on delete set null,
  handled_at timestamptz,
  created_at timestamptz not null default now(),
  unique (reporter_user_id, source_type, source_id)
);

create table if not exists public.marketplace_order_events (
  id bigint generated by default as identity primary key,
  order_type text not null check (order_type in ('merchant_retail')),
  order_id uuid not null,
  order_code text not null default '',
  actor_user_id uuid references auth.users(id) on delete set null,
  event_type text not null check (event_type in ('created','status_changed','dispute_opened','dispute_updated','dispute_closed','note')),
  previous_status text,
  next_status text,
  payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create table if not exists public.marketplace_order_disputes (
  id uuid primary key default gen_random_uuid(),
  order_type text not null check (order_type in ('merchant_retail')),
  order_id uuid not null,
  opened_by uuid not null references auth.users(id) on delete cascade,
  merchant_user_id uuid not null references auth.users(id) on delete cascade,
  reason text not null check (reason in ('not_received','not_as_described','damaged','merchant_cancelled','other')),
  detail text not null default '' check (char_length(detail) <= 1200),
  status text not null default 'open' check (status in ('open','under_review','resolved','closed')),
  resolution text,
  resolved_by uuid references auth.users(id) on delete set null,
  resolved_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (order_type, order_id)
);

create table if not exists public.merchant_platform_agreements (
  id uuid primary key default gen_random_uuid(),
  merchant_user_id uuid not null references auth.users(id) on delete cascade,
  agreement_key text not null default 'marketplace_terms',
  agreement_version text not null,
  accepted_at timestamptz not null default now(),
  accepted_ip text,
  unique (merchant_user_id, agreement_key, agreement_version)
);

create index if not exists marketplace_listing_controls_lookup_idx on public.marketplace_listing_controls(owner_user_id, source_type, source_id, status);
