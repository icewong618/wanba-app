alter table public.merchant_orders
  add column if not exists delivery_fee numeric(10,2) not null default 0,
  add column if not exists payment_provider text,
  add column if not exists payment_provider_reference text;

alter table public.merchant_orders
  drop constraint if exists merchant_orders_delivery_fee_check,
  add constraint merchant_orders_delivery_fee_check check (delivery_fee >= 0),
  drop constraint if exists merchant_orders_payment_status_check,
  add constraint merchant_orders_payment_status_check
    check (payment_status in ('unpaid','processing','paid','failed','refund_pending','refunded'));

alter table public.merchant_order_bills
  add column if not exists delivery_fee numeric(10,2) not null default 0,
  add column if not exists payment_provider text,
  add column if not exists payment_provider_reference text;

create table if not exists public.merchant_order_payment_attempts (
  id bigint generated by default as identity primary key,
  order_id uuid not null references public.merchant_orders(id) on delete cascade,
  merchant_user_id uuid not null references auth.users(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  provider text not null default 'stripe',
  provider_payment_id text not null unique,
  status text not null default 'requires_action'
    check (status in ('requires_action','processing','succeeded','failed','cancelled')),
  amount numeric(10,2) not null check (amount >= 0),
  currency text not null default 'usd',
  failure_message text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists merchant_order_payment_attempts_order_idx
  on public.merchant_order_payment_attempts (order_id, created_at desc);

alter table public.merchant_order_payment_attempts enable row level security;
grant select on public.merchant_order_payment_attempts to authenticated;

drop policy if exists "Customers can read own restaurant payment attempts"
  on public.merchant_order_payment_attempts;
drop policy if exists "Merchants can read restaurant payment attempts"
  on public.merchant_order_payment_attempts;
drop policy if exists "Restaurant payment attempts are visible to participants"
  on public.merchant_order_payment_attempts;
create policy "Restaurant payment attempts are visible to participants"
  on public.merchant_order_payment_attempts
  for select to authenticated
  using (
    user_id = auth.uid()
    or public.merchant_matrix_has_permission(merchant_user_id, 'order_manage')
    or public.merchant_matrix_has_permission(merchant_user_id, 'order_complete')
  );

