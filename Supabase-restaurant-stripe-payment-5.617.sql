create or replace function public.merchant_takeout_order_finalize_stripe_payment(
  p_order_id uuid,
  p_provider_payment_id text,
  p_payment_method text,
  p_amount numeric,
  p_payment_environment text default 'test'
)
returns public.merchant_order_bills
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_order public.merchant_orders%rowtype;
  v_bill public.merchant_order_bills%rowtype;
  v_expected numeric(12,2);
  v_paid_method text;
  v_remaining integer;
begin
  select * into v_order
  from public.merchant_orders
  where id = p_order_id
  for update;
  if not found then raise exception 'restaurant_order_not_found'; end if;
  if v_order.order_type <> 'takeout' then raise exception 'restaurant_order_not_takeout'; end if;
  if v_order.status = 'cancelled' then raise exception 'restaurant_order_cancelled'; end if;

  select * into v_bill
  from public.merchant_order_bills
  where order_id = p_order_id;
  if found then return v_bill; end if;

  v_expected := round(greatest(
    coalesce(v_order.subtotal,0)
    + coalesce(v_order.delivery_fee,0)
    + coalesce(v_order.tip_amount,0)
    - coalesce(v_order.quoted_discount_amount,0),
    0
  ),2);
  if abs(v_expected - round(greatest(coalesce(p_amount,0),0),2)) > 0.009 then
    raise exception 'restaurant_payment_amount_mismatch';
  end if;
  if coalesce(trim(p_provider_payment_id),'') = '' then
    raise exception 'restaurant_payment_reference_required';
  end if;

  v_paid_method := format(
    '%s_%s',
    case when p_payment_environment = 'live' then 'stripe' else 'stripe_test' end,
    coalesce(nullif(trim(p_payment_method),''),'card')
  );

  update public.merchant_coupon_claims
  set status = 'redeemed',
      redeemed_at = now(),
      redeemed_by = v_order.user_id
  where id in (
    select value::bigint
    from jsonb_array_elements_text(coalesce(v_order.coupon_claim_ids,'[]'::jsonb)) value
  )
    and merchant_user_id = v_order.merchant_user_id
    and user_id = v_order.user_id
    and status = 'claimed';

  select count(*) into v_remaining
  from public.merchant_order_items
  where order_id = p_order_id and not coalesce(is_served,false);

  update public.merchant_orders
  set payment_status = 'paid',
      paid_at = now(),
      paid_method = v_paid_method,
      payment_provider = 'stripe',
      payment_provider_reference = p_provider_payment_id,
      status = case when v_remaining = 0 then 'completed' else status end,
      completed_at = case when v_remaining = 0 then now() else completed_at end,
      updated_at = now()
  where id = p_order_id;

  insert into public.merchant_order_bills (
    order_id, merchant_user_id, user_id, payment_method, subtotal, delivery_fee,
    discount_amount, tip_amount, total_amount, coupon_claim_ids,
    payment_provider, payment_provider_reference
  ) values (
    p_order_id, v_order.merchant_user_id, v_order.user_id, v_paid_method,
    coalesce(v_order.subtotal,0), coalesce(v_order.delivery_fee,0),
    coalesce(v_order.quoted_discount_amount,0), coalesce(v_order.tip_amount,0),
    v_expected, coalesce(v_order.coupon_claim_ids,'[]'::jsonb),
    'stripe', p_provider_payment_id
  )
  on conflict (order_id) do update
  set payment_method = excluded.payment_method,
      subtotal = excluded.subtotal,
      delivery_fee = excluded.delivery_fee,
      discount_amount = excluded.discount_amount,
      tip_amount = excluded.tip_amount,
      total_amount = excluded.total_amount,
      coupon_claim_ids = excluded.coupon_claim_ids,
      payment_provider = excluded.payment_provider,
      payment_provider_reference = excluded.payment_provider_reference
  returning * into v_bill;

  return v_bill;
end;
$$;

revoke all on function public.merchant_takeout_order_finalize_stripe_payment(
  uuid,text,text,numeric,text
) from public, anon, authenticated;
grant execute on function public.merchant_takeout_order_finalize_stripe_payment(
  uuid,text,text,numeric,text
) to service_role;
