create or replace function public.redeem_merchant_reward(p_membership_id bigint,p_quantity integer)
returns jsonb language plpgsql security definer set search_path = public, pg_temp as $$
declare v_actor uuid:=auth.uid(); v_membership public.merchant_memberships%rowtype; v_target integer; v_reward text; v_before integer; v_deduct integer; v_after integer; v_transaction_id bigint;
begin
  if v_actor is null then raise exception 'not authenticated'; end if; if coalesce(p_quantity,0)<1 then raise exception 'quantity must be at least 1'; end if;
  select * into v_membership from public.merchant_memberships where id=p_membership_id for update;
  if not found or not public.merchant_matrix_has_role(v_membership.merchant_user_id,array['operator','clerk']) then raise exception 'not allowed'; end if;
  select greatest(1,coalesce(loyalty_target,8)),coalesce(nullif(trim(loyalty_reward),''),'会员奖励') into v_target,v_reward from public.merchants where user_id=v_membership.merchant_user_id limit 1;
  v_target:=coalesce(v_target,8); v_reward:=coalesce(v_reward,'会员奖励'); v_before:=greatest(0,coalesce(v_membership.stamp_count,0)); v_deduct:=v_target*p_quantity; if v_before<v_deduct then raise exception 'insufficient stamps'; end if; v_after:=v_before-v_deduct;
  update public.merchant_memberships set stamp_count=v_after,updated_at=now() where id=v_membership.id;
  insert into public.merchant_member_transactions (membership_id,merchant_user_id,operator_user_id,user_id,action,delta,before_value,after_value,source,note,transaction_kind)
  values (v_membership.id,v_membership.merchant_user_id,v_actor,v_membership.user_id,'stamp',-v_deduct,v_before,v_after,'manual',format('奖励兑换：%s 份「%s」，每份扣 %s 次',p_quantity,v_reward,v_target),'reward_redeem') returning id into v_transaction_id;
  return jsonb_build_object('membership_id',v_membership.id,'transaction_id',v_transaction_id,'before_value',v_before,'after_value',v_after,'quantity',p_quantity,'reward',v_reward);
end;
$$;

create or replace function public.revert_merchant_reward_redemption(p_transaction_id bigint)
returns jsonb language plpgsql security definer set search_path = public, pg_temp as $$
declare v_actor uuid:=auth.uid(); v_redemption public.merchant_member_transactions%rowtype; v_membership public.merchant_memberships%rowtype; v_before integer; v_restore integer; v_after integer; v_reversal_id bigint;
begin
  if v_actor is null then raise exception 'not authenticated'; end if; select * into v_redemption from public.merchant_member_transactions where id=p_transaction_id for update;
  if not found or not public.merchant_matrix_has_role(v_redemption.merchant_user_id,array['operator','clerk']) then raise exception 'not allowed'; end if;
  if v_redemption.transaction_kind<>'reward_redeem' or v_redemption.revoked_at is not null then raise exception 'redemption_not_reversible'; end if; v_restore:=abs(coalesce(v_redemption.delta,0)); if v_restore<1 then raise exception 'invalid redemption amount'; end if;
  select * into v_membership from public.merchant_memberships where id=v_redemption.membership_id for update; if not found then raise exception 'membership not found'; end if; v_before:=greatest(0,coalesce(v_membership.stamp_count,0)); v_after:=v_before+v_restore;
  update public.merchant_memberships set stamp_count=v_after,updated_at=now() where id=v_membership.id; update public.merchant_member_transactions set revoked_at=now(),revoked_by=v_actor where id=v_redemption.id;
  insert into public.merchant_member_transactions (membership_id,merchant_user_id,operator_user_id,user_id,action,delta,before_value,after_value,source,note,transaction_kind,reversal_of_id)
  values (v_membership.id,v_membership.merchant_user_id,v_actor,v_membership.user_id,'stamp',v_restore,v_before,v_after,'manual',format('撤销奖励兑换：恢复 %s 次消费记录',v_restore),'reward_reversal',v_redemption.id) returning id into v_reversal_id;
  return jsonb_build_object('membership_id',v_membership.id,'redemption_id',v_redemption.id,'reversal_id',v_reversal_id,'before_value',v_before,'after_value',v_after);
end;
$$;

create or replace function public.redeem_merchant_coupon_claim(p_claim_id bigint)
returns public.merchant_coupon_claims language plpgsql security definer set search_path = public, pg_temp as $$
declare claim_row public.merchant_coupon_claims; merchant_coupons jsonb;
begin
  if auth.uid() is null then raise exception 'not_authenticated'; end if; select * into claim_row from public.merchant_coupon_claims where id=p_claim_id for update; if not found then raise exception 'coupon_claim_not_found'; end if;
  if not public.merchant_matrix_has_role(claim_row.merchant_user_id,array['operator','clerk']) then raise exception 'not_coupon_merchant'; end if; if claim_row.status<>'claimed' then raise exception 'coupon_already_redeemed'; end if;
  select coupons into merchant_coupons from public.merchants where user_id=claim_row.merchant_user_id and verified=true; if merchant_coupons is null then raise exception 'merchant_not_verified'; end if;
  if not exists (select 1 from jsonb_array_elements(merchant_coupons) with ordinality as item(value,ord) where coalesce(item.value->>'id','legacy-'||(item.ord-1)::text)=claim_row.coupon_id and coalesce(item.value->>'active','true')<>'false') then raise exception 'coupon_not_available'; end if;
  update public.merchant_coupon_claims set status='redeemed',redeemed_at=now(),redeemed_by=auth.uid() where id=claim_row.id returning * into claim_row; return claim_row;
end;
$$;
