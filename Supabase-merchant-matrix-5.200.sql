-- 乐生活 5.200：商家矩阵账号
-- 个人账号可被授权进入认证商家的会员核销工作台。

create table if not exists public.merchant_team_members (
  id bigint generated by default as identity primary key,
  merchant_user_id uuid not null references auth.users(id) on delete cascade,
  member_user_id uuid not null references auth.users(id) on delete cascade,
  role text not null default 'clerk' check (role in ('operator', 'clerk')),
  status text not null default 'pending' check (status in ('pending', 'active', 'declined', 'revoked')),
  invited_by uuid not null references auth.users(id) on delete cascade,
  invited_at timestamptz not null default now(),
  responded_at timestamptz,
  revoked_at timestamptz,
  updated_at timestamptz not null default now(),
  unique (merchant_user_id, member_user_id)
);

create index if not exists merchant_team_members_merchant_idx on public.merchant_team_members (merchant_user_id, status, updated_at desc);
create index if not exists merchant_team_members_member_idx on public.merchant_team_members (member_user_id, status, updated_at desc);
alter table public.merchant_team_members enable row level security;
grant select on public.merchant_team_members to authenticated;
grant usage, select on sequence public.merchant_team_members_id_seq to authenticated;

create or replace function public.merchant_matrix_has_role(p_merchant_user_id uuid, p_allowed_roles text[] default array['operator', 'clerk']::text[])
returns boolean language sql stable security definer set search_path = public, pg_temp as $$
  select auth.uid() = p_merchant_user_id
  or exists (select 1 from public.merchant_team_members t where t.merchant_user_id = p_merchant_user_id and t.member_user_id = auth.uid() and t.status = 'active' and t.role = any (p_allowed_roles));
$$;
revoke all on function public.merchant_matrix_has_role(uuid, text[]) from public;
grant execute on function public.merchant_matrix_has_role(uuid, text[]) to authenticated;

drop policy if exists "merchant team owner or member reads 5.200" on public.merchant_team_members;
create policy "merchant team owner or member reads 5.200" on public.merchant_team_members for select to authenticated
using ((select auth.uid()) = merchant_user_id or (select auth.uid()) = member_user_id);

create or replace function public.merchant_matrix_my_access()
returns table (team_member_id bigint, merchant_user_id uuid, role text, merchant jsonb)
language sql stable security definer set search_path = public, pg_temp as $$
  select t.id, t.merchant_user_id, t.role,
    jsonb_build_object('user_id',m.user_id,'business_name',m.business_name,'logo',m.logo,'cover_image',m.cover_image,'address',m.address,'verified',m.verified,'loyalty_target',m.loyalty_target,'loyalty_reward',m.loyalty_reward,'points_per_visit',m.points_per_visit,'membership_tiers',m.membership_tiers,'birthday_reward',m.birthday_reward)
  from public.merchant_team_members t join public.merchants m on m.user_id = t.merchant_user_id
  where t.member_user_id = auth.uid() and t.status = 'active' and coalesce(m.verified,false) = true order by t.updated_at desc;
$$;

create or replace function public.merchant_matrix_search_people(p_query text)
returns table (user_id uuid, name text, avatar text)
language sql stable security definer set search_path = public, pg_temp as $$
  select p.user_id, coalesce(nullif(p.name,''),'乐生活用户'), p.avatar from public.profiles p
  where auth.uid() is not null and length(trim(coalesce(p_query,''))) >= 1 and p.user_id <> auth.uid() and p.name ilike '%' || trim(p_query) || '%'
  order by p.updated_at desc nulls last limit 8;
$$;

create or replace function public.merchant_matrix_invite_member(p_member_user_id uuid, p_role text default 'clerk')
returns public.merchant_team_members language plpgsql security definer set search_path = public, pg_temp as $$
declare v_row public.merchant_team_members;
begin
  if auth.uid() is null then raise exception 'not_authenticated'; end if;
  if p_member_user_id is null or p_member_user_id = auth.uid() then raise exception 'invalid_member'; end if;
  if p_role not in ('operator','clerk') then raise exception 'invalid_role'; end if;
  if not exists (select 1 from public.merchants where user_id = auth.uid() and verified = true) then raise exception 'merchant_not_verified'; end if;
  if not exists (select 1 from public.profiles where user_id = p_member_user_id) then raise exception 'member_not_found'; end if;
  insert into public.merchant_team_members (merchant_user_id,member_user_id,role,status,invited_by,invited_at,responded_at,revoked_at,updated_at)
  values (auth.uid(),p_member_user_id,p_role,'pending',auth.uid(),now(),null,null,now())
  on conflict (merchant_user_id,member_user_id) do update set role=excluded.role,status='pending',invited_by=auth.uid(),invited_at=now(),responded_at=null,revoked_at=null,updated_at=now() returning * into v_row;
  return v_row;
end;
$$;

create or replace function public.merchant_matrix_respond_invite(p_team_member_id bigint, p_accept boolean)
returns public.merchant_team_members language plpgsql security definer set search_path = public, pg_temp as $$
declare v_row public.merchant_team_members;
begin
  if auth.uid() is null then raise exception 'not_authenticated'; end if;
  update public.merchant_team_members set status=case when p_accept then 'active' else 'declined' end,responded_at=now(),updated_at=now() where id=p_team_member_id and member_user_id=auth.uid() and status='pending' returning * into v_row;
  if not found then raise exception 'invite_not_found'; end if; return v_row;
end;
$$;

create or replace function public.merchant_matrix_revoke_member(p_team_member_id bigint)
returns public.merchant_team_members language plpgsql security definer set search_path = public, pg_temp as $$
declare v_row public.merchant_team_members;
begin
  if auth.uid() is null then raise exception 'not_authenticated'; end if;
  update public.merchant_team_members set status='revoked',revoked_at=now(),updated_at=now() where id=p_team_member_id and merchant_user_id=auth.uid() returning * into v_row;
  if not found then raise exception 'team_member_not_found'; end if; return v_row;
end;
$$;

